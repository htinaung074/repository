package com.htinaung.DTO;


public class CourseDTO {
	
	private String courseNumber;
	private String courseName;
	private String year;
	private String month;
	private String day;
	private String openHr;
	private String openMin;
	private String endHr;
	private String endMin;
	private String capicity;
	public String getCourseNumber() {
		return courseNumber;
	}
	public void setCourseNumber(String courseNumber) {
		this.courseNumber = courseNumber;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}

	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getOpenHr() {
		return openHr;
	}
	public void setOpenHr(String openHr) {
		this.openHr = openHr;
	}

	public String getOpenMin() {
		return openMin;
	}
	public void setOpenMin(String openMin) {
		this.openMin = openMin;
	}
	public String getEndHr() {
		return endHr;
	}
	public void setEndHr(String endHr) {
		this.endHr = endHr;
	}
	public String getEndMin() {
		return endMin;
	}
	public void setEndMin(String endMin) {
		this.endMin = endMin;
	}
	public String getCapicity() {
		return capicity;
	}
	public void setCapicity(String capicity) {
		this.capicity = capicity;
	}

}
